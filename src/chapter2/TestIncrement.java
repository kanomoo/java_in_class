package chapter2;

public class TestIncrement {
    public static void main(String[] args) {
        int c;
        c = 5; // postincrement
        System.out.println(c);
        System.out.println(c++);
        System.out.println(c);
        c = 5;
        System.out.println(c);
        System.out.println(++c);
        System.out.println(c);
    }
}
